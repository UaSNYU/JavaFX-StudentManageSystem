package org.example.myprj;

public class ScheduleUnitInfo {
    public String CourseName;
    public String Day1;
    public String Day2;
    public String Day3;
    public String Day4;
    public String Day5;

    ScheduleUnitInfo(String CourseName, String Day1, String Day2, String Day3, String Day4, String Day5) {
        {
            this.CourseName = CourseName;
            this.Day1 = Day1;
            this.Day2 = Day2;
            this.Day3 = Day3;
            this.Day4 = Day4;
            this.Day5 = Day5;
        }
    }
}
